                <!-- Section: Costs -->
                <section id="cost" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">Costs and Copays</h2>
                    <p class="mb-4">
                        One of the biggest advantages of Medicaid coverage is the low cost to the patient.
                    </p>
                    <ul class="list-disc pl-6 space-y-2 text-gray-700 mb-6">
                        <li><strong>Zero to Low Copay:</strong> In many cases, if approved, Medicaid covers 100% of the cost of TMS treatment. Some plans may have small nominal copays.</li>
                        <li><strong>Prior Authorization:</strong> Medicaid almost always requires "Prior Authorization." This means your doctor must submit paperwork proving you meet the criteria <em>before</em> treatment starts. Once that authorization is granted, the billing is covered.</li>
                    </ul>
                    <p class="mb-6">
                         This makes TMS accessible to many who could not otherwise afford the out-of-pocket costs of advanced mental health treatments.
                    </p>
