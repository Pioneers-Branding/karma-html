                <!-- Section: Medicaid Basics -->
                <section id="medicaid-basics" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">Medicaid and TMS: The Basics</h2>
                    <p class="mb-6">
                        <a href="tms-therapy.php" class="text-primary hover:underline">TMS Therapy</a> has become a gold-standard treatment for treatment-resistant depression. Recognizing its effectiveness and cost-efficiency compared to long-term hospitalization or disability, most Medicaid programs have added it to their covered benefits.
                    </p>
                    <div class="bg-blue-50 border-l-4 border-blue-200 p-6 rounded-r-xl mb-6">
                        <h3 class="font-bold text-blue-900 mb-2">Key Takeaway</h3>
                        <p class="text-blue-900">
                           Medicaid views TMS as a medical necessity for patients who haven't found relief from standard antidepressants. It is <strong>not</strong> considered experimental.
                        </p>
                    </div>
                </section>
                
                <!-- Section: Requirements -->
                <section id="requirements" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">Standard Coverage Requirements</h2>
                    <p class="mb-6">
                        To get approved for TMS under Medicaid (or Medi-Cal), you typically need to meet the following criteria. These are designed to ensure that TMS is used for cases where it is most needed—specifically, <a href="tms-therapy-for-treatment-resistant-depression.php" class="text-primary hover:underline">Treatment-Resistant Depression (TRD)</a>.
                    </p>
                    
                    <h3 class="text-2xl font-bold text-gray-900 mb-4 mt-8">1. Diagnosis</h3>
                    <ul class="list-disc pl-6 space-y-2 text-gray-700 mb-6">
                        <li>You must have a primary diagnosis of <a href="depression.php" class="text-primary hover:underline">Major Depressive Disorder (MDD)</a>, severe or recurrent.</li>
                        <li>Some states may cover it for other conditions, but MDD is the primary covered indication.</li>
                    </ul>

                    <h3 class="text-2xl font-bold text-gray-900 mb-4">2. Medication Trials</h3>
                    <p class="mb-4">You usually need to show that antidepressants haven't worked for you. Typical requirements include:</p>
                    <ul class="list-disc pl-6 space-y-2 text-gray-700 mb-6">
                        <li>Evidence of failure (meaning lack of significant improvement) on <strong>2 to 4 different <a href="medication-management.php" class="text-primary hover:underline">antidepressant medications</a></strong> from distinct classes.</li>
                        <li>Evidence that you took these medications at the therapeutic dose for an adequate duration.</li>
                    </ul>

                    <h3 class="text-2xl font-bold text-gray-900 mb-4">3. Psychotherapy</h3>
                    <p class="mb-4">Most Medicaid plans require that you have tried evidence-based psychotherapy (talk therapy) without sufficient success.</p>

                    <h3 class="text-2xl font-bold text-gray-900 mb-4">4. No Contraindications</h3>
                    <p class="mb-4">You must not have conditions that make TMS unsafe, such as:</p>
                    <ul class="list-disc pl-6 space-y-2 text-gray-700 mb-6">
                        <li>Non-removable conductive metal in or near the head (cochlear implants, aneurysm clips, etc.).</li>
                        <li>History of seizure disorders (evaluated on a case-by-case basis).</li>
                    </ul>
                </section>

                <!-- Section: How to Check -->
                <section id="how-to-check" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">How to Check Your Specific Coverage</h2>
                    <p class="mb-6">
                        Since plans vary, verification is critical. Here is how you can find out for sure:
                    </p>
                    <div class="bg-gray-50 p-6 rounded-xl mb-6">
                        <ol class="list-decimal pl-6 space-y-4 text-gray-700">
                            <li><strong>Check the Back of Your Card:</strong> Call the Member Services number listed on your Medicaid card.</li>
                            <li><strong>Ask About CPT Codes:</strong> Ask specifically if they cover CPT codes <strong>90867, 90868, and 90869</strong>. These are the standard medical codes for TMS therapy.</li>
                            <li><strong>Contact a TMS Provider:</strong> Clinics like <a href="contact-us.php" class="text-primary hover:underline">Karma TMS</a> have dedicated insurance specialists. We can run a "benefits check" for you specifically for TMS services, often within 24 hours.</li>
                        </ol>
                    </div>
