                        For millions of Americans battling <a href="depression.php" class="text-primary hover:underline">severe depression</a>, finding an effective treatment that is also affordable can feel like an uphill battle. If you rely on Medicaid, you might be asking: <strong>Does Medicaid cover TMS therapy?</strong>
                    </p>
                    <p class="mt-4 text-gray-700">
                        The short answer is: <strong>Yes, in most states.</strong> Medicaid generally covers Transcranial Magnetic Stimulation (TMS) for Major Depressive Disorder (MDD), provided you meet specific medical necessity criteria.
                    </p>
                    <p class="text-gray-700">
                        However, because Medicaid is administered state-by-state (such as Medi-Cal in California), the exact rules can vary. This guide will walk you through the standard requirements, how to verify your benefits, and what to expect during the process.
