        function scrollToSection(id) {
            const element = document.getElementById(id);
            if (element) {
                const offset = 120; // Adjust based on sticky header height
                const bodyRect = document.body.getBoundingClientRect().top;
                const elementRect = element.getBoundingClientRect().top;
                const elementPosition = elementRect - bodyRect;
                const offsetPosition = elementPosition - offset;

                window.scrollTo({
                    top: offsetPosition,
                    behavior: 'smooth'
                });

                // Set active state on click
                const navButtons = document.querySelectorAll('.toc-button');
                navButtons.forEach(button => {
                    button.classList.remove('active');
                    if (button.getAttribute('onclick').includes(id)) {
                        button.classList.add('active');
                    }
                });
            }
        }

        // FAQ Toggle Logic
        function toggleBlogFAQ(button) {
            const item = button.parentElement;
            const content = item.querySelector('.accordion-content');
            const isOpen = item.getAttribute('data-state') === 'open';
            
            // Close all other items
            document.querySelectorAll('.accordion-item').forEach(otherItem => {
                if (otherItem !== item) {
                    otherItem.setAttribute('data-state', 'closed');
                    otherItem.querySelector('.accordion-content').style.maxHeight = '0px';
                }
            });

            // Toggle current item
            if (isOpen) {
                item.setAttribute('data-state', 'closed');
                content.style.maxHeight = '0px';
            } else {
                item.setAttribute('data-state', 'open');
                content.style.maxHeight = content.scrollHeight + 'px';
            }
        }
