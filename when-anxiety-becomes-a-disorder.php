                <!-- Types -->
                <section id="types" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">The 6 Most Common Types of Anxiety Disorders</h2>
                    <p class="mb-6">
                        Anxiety disorders are not a single condition — they are a family of related but distinct diagnoses. Here are the most prevalent:
                    </p>

                    <div class="disorder-card">
                        <h3>1. Generalized Anxiety Disorder (GAD)</h3>
                        <p class="text-gray-700 text-sm mb-3"><strong>Core feature:</strong> Excessive, uncontrollable worry about multiple everyday topics (health, money, work, family) on more days than not for at least 6 months.</p>
                        <p class="text-gray-700 text-sm">GAD is among the most common anxiety disorders, affecting approximately 6.8 million American adults. People with GAD often describe it as their mind "always looking for something to worry about." Physical symptoms include muscle tension, fatigue, headaches, and sleep disruption.</p>
                    </div>

                    <div class="disorder-card">
                        <h3>2. Panic Disorder</h3>
                        <p class="text-gray-700 text-sm mb-3"><strong>Core feature:</strong> Recurrent, unexpected panic attacks followed by persistent concern about future attacks or significant behavioral change due to attacks.</p>
                        <p class="text-gray-700 text-sm">A panic attack is an abrupt surge of intense fear with symptoms that peak within minutes: heart palpitations, sweating, trembling, shortness of breath, chest pain, dizziness, and a feeling of losing control or dying. After attacks, many people develop significant avoidance of situations they fear might trigger another episode.</p>
                    </div>

                    <div class="disorder-card">
                        <h3>3. Social Anxiety Disorder (Social Phobia)</h3>
                        <p class="text-gray-700 text-sm mb-3"><strong>Core feature:</strong> Intense, persistent fear of social or performance situations in which the person fears they will be scrutinized, judged, humiliated, or embarrassed.</p>
                        <p class="text-gray-700 text-sm">Social anxiety disorder is far more than shyness. It can prevent people from attending social events, speaking up at work, eating in public, making phone calls, or forming relationships. It affects approximately 15 million adults in the US and often develops in adolescence.</p>
                    </div>

                    <div class="disorder-card">
                        <h3>4. Specific Phobias</h3>
                        <p class="text-gray-700 text-sm mb-3"><strong>Core feature:</strong> Marked, excessive, and persistent fear of a specific object or situation (heights, flying, needles, animals, blood, etc.) that is disproportionate to the actual danger.</p>
                        <p class="text-gray-700 text-sm">Specific phobias are the most prevalent anxiety disorders globally, affecting 12.5% of Americans at some point. Exposure to or anticipation of the phobic stimulus provokes an immediate anxiety response, often leading to significant avoidance that impairs daily life.</p>
                    </div>

                    <div class="disorder-card">
                        <h3>5. Agoraphobia</h3>
                        <p class="text-gray-700 text-sm mb-3"><strong>Core feature:</strong> Marked fear or anxiety about two or more situations where escape might be difficult or help might be unavailable if panic symptoms occur (public transport, open/enclosed spaces, crowds, being outside alone).</p>
                        <p class="text-gray-700 text-sm">Contrary to popular belief, agoraphobia is not simply "fear of open spaces." It is a complex disorder centered on the fear of being trapped or unable to escape during a panic attack. In severe cases, people become unable to leave their homes at all.</p>
                    </div>

                    <div class="disorder-card">
                        <h3>6. Separation Anxiety Disorder</h3>
                        <p class="text-gray-700 text-sm mb-3"><strong>Core feature:</strong> Excessive fear or anxiety concerning separation from attachment figures, disproportionate to developmental level.</p>
                        <p class="text-gray-700 text-sm">While commonly associated with children, separation anxiety disorder also affects adults. Adults with this condition experience excessive worry about something bad happening to loved ones when apart, persistent reluctance to go out due to fear of separation, and nightmares about separation.</p>
                    </div>

                    <div class="bg-blue-50 border-l-4 border-blue-200 p-6 rounded-r-xl mt-6">
                        <h4 class="font-bold text-blue-900 mb-2">Note on OCD and PTSD</h4>
                        <p class="text-blue-900 text-sm">Obsessive-Compulsive Disorder <a href="ocd.php" class="hover:underline font-medium">(OCD)</a> and Post-Traumatic Stress Disorder <a href="ptsd.php" class="hover:underline font-medium">(PTSD)</a> were previously classified under anxiety disorders but now have their own categories in DSM-5. Both involve significant anxiety components and are frequently co-diagnosed with anxiety disorders.</p>
                    </div>
                </section>

