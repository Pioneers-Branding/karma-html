                <section id="causes" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">What Causes Anxiety Disorders?</h2>
                    <p class="mb-6">Anxiety disorders are multifactorial — no single cause explains them. Current research points to a complex interplay of:</p>

                    <div class="grid md:grid-cols-2 gap-6 mb-6">
                        <div class="bg-white p-6 rounded-xl border border-gray-200 shadow-sm">
                            <h3 class="font-bold text-gray-900 mb-2 flex items-center gap-2"><i data-lucide="dna" class="w-5 h-5 text-primary"></i>Genetics</h3>
                            <p class="text-gray-600 text-sm">Anxiety disorders run in families. Twin studies suggest a heritability of approximately 30–40% for generalized anxiety. Having a first-degree relative with an anxiety disorder increases your risk significantly.</p>
                        </div>
                        <div class="bg-white p-6 rounded-xl border border-gray-200 shadow-sm">
                            <h3 class="font-bold text-gray-900 mb-2 flex items-center gap-2"><i data-lucide="brain" class="w-5 h-5 text-primary"></i>Brain Chemistry & Structure</h3>
                            <p class="text-gray-600 text-sm">Dysregulation in neurotransmitter systems (serotonin, GABA, norepinephrine) and overactivity in the amygdala (the brain's fear center) are consistently associated with anxiety disorders, as shown in neuroimaging studies.</p>
                        </div>
                        <div class="bg-white p-6 rounded-xl border border-gray-200 shadow-sm">
                            <h3 class="font-bold text-gray-900 mb-2 flex items-center gap-2"><i data-lucide="life-buoy" class="w-5 h-5 text-primary"></i>Trauma & Life Experience</h3>
                            <p class="text-gray-600 text-sm">Adverse childhood experiences (ACEs), trauma, prolonged stress, abuse, neglect, and major life transitions all substantially increase anxiety disorder risk. Trauma memory consolidation can sensitize the fear circuits permanently.</p>
                        </div>
                        <div class="bg-white p-6 rounded-xl border border-gray-200 shadow-sm">
                            <h3 class="font-bold text-gray-900 mb-2 flex items-center gap-2"><i data-lucide="person-standing" class="w-5 h-5 text-primary"></i>Personality & Temperament</h3>
                            <p class="text-gray-600 text-sm">Individuals with behavioral inhibition in childhood (shy, easily startled, risk-averse) have higher rates of anxiety disorders in adulthood. Perfectionism, neuroticism, and high harm-avoidance are also risk-factor traits.</p>
                        </div>
                    </div>
                </section>

