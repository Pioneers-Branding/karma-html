                    <h2 class="text-3xl font-bold text-primary mb-6">Warning Signs That Your Anxiety Has Become a Disorder</h2>
                    <p class="mb-6">The following signs suggest that what you're experiencing has moved beyond normal, healthy anxiety and may warrant a professional clinical evaluation:</p>

