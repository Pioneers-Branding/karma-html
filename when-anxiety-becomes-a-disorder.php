                    <h2 class="text-3xl font-bold text-primary mb-6">The Risks of Leaving Anxiety Disorders Untreated</h2>
                    <p class="mb-6">
                        The <a href="https://www.nimh.nih.gov/health/topics/anxiety-disorders" target="_blank" rel="noopener noreferrer" class="text-primary hover:underline">National Institute of Mental Health</a> estimates that anxiety disorders are among the most undertreated mental health conditions despite being among the most treatable. Delaying care carries serious consequences:
                    </p>
                    <ul class="list-disc pl-6 space-y-3 text-gray-700 mb-6">
                        <li><strong>Co-occurring depression:</strong> Up to 50% of people with an anxiety disorder also develop <a href="depression.php" class="text-primary hover:underline">major depression</a>. The two conditions worsen each other when untreated simultaneously.</li>
                        <li><strong>Substance misuse:</strong> Many individuals with untreated anxiety turn to alcohol or drugs for relief. Approximately 20% of people with anxiety disorders have a co-occurring substance use disorder.</li>
                        <li><strong>Physical health deterioration:</strong> Chronic anxiety activates the HPA axis and sympathetic nervous system continuously, contributing to hypertension, cardiovascular disease, a weakened immune system, and gastrointestinal disorders.</li>
                        <li><strong>Social isolation:</strong> Avoidance behaviors compound over time, progressively limiting social connection, career advancement, and quality of life.</li>
                        <li><strong>Worsening disorder severity:</strong> Untreated anxiety typically does not resolve on its own — it tends to worsen or expand into additional anxiety domains over time without clinical intervention.</li>
                    </ul>
                </section>

