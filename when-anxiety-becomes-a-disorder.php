                        <div class="bg-white p-5 rounded-xl border border-gray-200 shadow-sm">
                            <h3 class="font-bold text-gray-900 mb-2 flex items-center gap-2"><i data-lucide="alert-circle" class="w-5 h-5 text-primary"></i>Worry You Cannot Turn Off</h3>
                            <p class="text-gray-600 text-sm">You worry excessively about multiple areas of life (health, finances, relationships, work) even when there's no concrete reason to, and you find it nearly impossible to redirect your thoughts.</p>
                        </div>
                        <div class="bg-white p-5 rounded-xl border border-gray-200 shadow-sm">
                            <h3 class="font-bold text-gray-900 mb-2 flex items-center gap-2"><i data-lucide="alert-circle" class="w-5 h-5 text-primary"></i>Physical Symptoms Without Medical Cause</h3>
                            <p class="text-gray-600 text-sm">Chronic muscle tension, headaches, gastrointestinal distress, chest tightness, or fatigue that your doctor cannot attribute to a medical condition are very common physical manifestations of anxiety disorders.</p>
                        </div>
                        <div class="bg-white p-5 rounded-xl border border-gray-200 shadow-sm">
                            <h3 class="font-bold text-gray-900 mb-2 flex items-center gap-2"><i data-lucide="alert-circle" class="w-5 h-5 text-primary"></i>Avoidance That Shrinks Your Life</h3>
                            <p class="text-gray-600 text-sm">You avoid situations, places, people, or activities because of fear or anticipated anxiety. Over time, this avoidance circle grows, and your world becomes progressively smaller.</p>
                        </div>
                        <div class="bg-white p-5 rounded-xl border border-gray-200 shadow-sm">
                            <h3 class="font-bold text-gray-900 mb-2 flex items-center gap-2"><i data-lucide="alert-circle" class="w-5 h-5 text-primary"></i>Panic Attacks</h3>
                            <p class="text-gray-600 text-sm">Sudden, intense surges of fear accompanied by physical symptoms (racing heart, shortness of breath, dizziness, numbness) that peak within minutes. Even one panic attack can trigger anticipatory anxiety about future attacks.</p>
                        </div>
                        <div class="bg-white p-5 rounded-xl border border-gray-200 shadow-sm">
                            <h3 class="font-bold text-gray-900 mb-2 flex items-center gap-2"><i data-lucide="alert-circle" class="w-5 h-5 text-primary"></i>Sleep Disruption</h3>
                            <p class="text-gray-600 text-sm">Difficulty falling or staying asleep due to racing thoughts, worry, or heightened physical arousal. Chronic sleep deprivation then feeds and amplifies anxiety in a destructive cycle.</p>
                        </div>
                        <div class="bg-white p-5 rounded-xl border border-gray-200 shadow-sm">
                            <h3 class="font-bold text-gray-900 mb-2 flex items-center gap-2"><i data-lucide="alert-circle" class="w-5 h-5 text-primary"></i>Impaired Daily Functioning</h3>
                            <p class="text-gray-600 text-sm">Your anxiety is significantly interfering with your ability to do your job, maintain relationships, care for yourself, or enjoy your life. This functional impairment is a key clinical threshold indicator.</p>
                        </div>
                        <div class="bg-white p-5 rounded-xl border border-gray-200 shadow-sm">
                            <h3 class="font-bold text-gray-900 mb-2 flex items-center gap-2"><i data-lucide="alert-circle" class="w-5 h-5 text-primary"></i>Duration of 6+ Months</h3>
                            <p class="text-gray-600 text-sm">For most anxiety disorder diagnoses, the DSM-5 requires symptoms to be present for at least 6 months. If your anxiety has been chronic and persistent for this period, a formal evaluation is warranted.</p>
