                    <p class="text-xl leading-relaxed text-gray-700">
                        Stress is an inevitable part of life, but how we manage it can make a significant difference in our mental and physical well-being. Unchecked stress can lead to burnout, anxiety, and physical health issues.
                    </p>
                    <p class="text-xl leading-relaxed text-gray-700">
                        At Karma TMS, we advocate for the <strong>Four A’s of Stress Management</strong>: <strong>Avoid, Alter, Adapt, and Accept</strong>. These strategies provide a simple yet powerful framework for dealing with stressors in a healthy and proactive manner.
                    </p>
                </div>

                <!-- Section 1: Avoid -->
                <section id="avoid" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">1. Avoid the Stressor</h2>
                    
                    <div class="bg-blue-50 border border-blue-200 rounded-xl p-6 mb-6">
                        <p class="text-gray-700 mb-0">
                            <strong>Principle:</strong> Not all stress can be avoided, but many unnecessary stressors can be eliminated. Prevention is often the best cure.
                        </p>
                    </div>

                    <ul class="list-disc pl-6 space-y-4 mb-6">
                        <li>
                            <strong>Learn to say "no":</strong> Know your limits and stick to them. Taking on more than you can handle—whether in your personal or professional life—is a recipe for stress.
                        </li>
                        <li>
                            <strong>Avoid people who stress you out:</strong> If someone consistently causes stress in your life and you can't turn the relationship around, limit the amount of time you spend with them or end the relationship entirely.
                        </li>
                        <li>
                            <strong>Take control of your environment:</strong> If the news makes you anxious, turn off the TV. If traffic is a stressor, find a longer but less-traveled route. If going to the market is an unpleasant chore, do your grocery shopping online.
                        </li>
                    </ul>
                </section>

                <!-- Section 2: Alter -->
                <section id="alter" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">2. Alter the Stressor</h2>

                    <p class="mb-6">
                        If you can't avoid a stressful situation, the <strong>4A's of stress management</strong> suggests trying to alter it. Often, this involves changing the way you communicate and operate in your daily life.
                    </p>

                    <ul class="list-disc pl-6 space-y-4 mb-6">
                        <li>
                            <strong>Express your feelings instead of bottling them up:</strong> If something or someone is bothering you, be more assertive and communicate your concerns in an open and respectful way. If you don't voice your feelings, resentment will build and the situation will likely remain the same.
                        </li>
                        <li>
                            <strong>Be willing to compromise:</strong> When you ask someone to change their behavior, be willing to do the same. If you both are willing to bend at least a little, you’ll have a good chance of finding a happy middle ground.
                        </li>
                        <li>
                            <strong>Create a balanced schedule:</strong> All work and no play is a recipe for burnout. Try to find a balance between work and family life, social activities and solitary pursuits, daily responsibilities and downtime.
                        </li>
                    </ul>
                </section>

                <!-- Section 3: Adapt -->
                <section id="adapt" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">3. Adapt to the Stressor</h2>
                    
                    <div class="bg-amber-50 border border-amber-200 rounded-xl p-6 mb-6">
                        <p class="text-gray-700 mb-0">
                            <strong>Principle:</strong> If you can't change the stressor, change yourself. You can adapt to stressful situations and regain your sense of control by changing your expectations and attitude.
                        </p>
                    </div>

                    <ul class="list-disc pl-6 space-y-4 mb-6">
                        <li>
                            <strong>Reframe problems:</strong> Try to view stressful situations from a more positive perspective. Rather than fuming about a traffic jam, look at it as an opportunity to pause and regroup, listen to your favorite radio station, or enjoy some alone time.
                        </li>
                        <li>
                            <strong>Look at the big picture:</strong> Take perspective of the stressful situation. Ask yourself how important it will be in the long run. Will it matter in a month? A year? Is it really worth getting upset over? If the answer is no, focus your time and energy elsewhere.
                        </li>
                        <li>
                            <strong>Adjust your standards:</strong> Perfectionism is a major source of avoidable stress. Stop setting yourself up for failure by demanding perfection. Set reasonable standards for yourself and others, and learn to be okay with "good enough."
                        </li>
                    </ul>
                </section>

                <!-- Section 4: Accept -->
                <section id="accept" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">4. Accept the Stressor</h2>
                    
                    <p class="mb-6">
                        Some sources of stress are unavoidable. You can't prevent or change stressors such as the death of a loved one, a serious illness, or a national recession. In such cases, the best way to cope with stress is to accept things as they are.
                    </p>

                    <div class="grid md:grid-cols-2 gap-6 mb-8">
                        <div class="bg-white border text-card-foreground shadow-sm border-l-4 border-purple-500 rounded-xl p-6">
                            <h4 class="font-bold text-primary mb-2">Don't Control the Uncontrollable</h4>
                            <p class="text-sm text-gray-700">Many things in life are beyond our control, particularly the behavior of other people. Rather than stressing over them, focus on the things you can control such as the way you choose to react to problems.</p>
                        </div>
                        <div class="bg-white border text-card-foreground shadow-sm border-l-4 border-purple-500 rounded-xl p-6">
                            <h4 class="font-bold text-primary mb-2">Look for the Upside</h4>
                            <p class="text-sm text-gray-700">As the saying goes, "What doesn’t kill us makes us stronger." When facing major challenges, try to look at them as opportunities for personal growth. If your own poor choices contributed to a stressful situation, reflect on them and learn from your mistakes.</p>
                        </div>
                    </div>
                    
                    <ul class="list-disc pl-6 space-y-4 mb-6">
                         <li>
                            <strong>Learn to forgive:</strong> Accept the fact that we live in an imperfect world and that people make mistakes. Let go of anger and resentments. Free yourself from negative energy by forgiving and moving on.
                        </li>
                    </ul>
                </section>

