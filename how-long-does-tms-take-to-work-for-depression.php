                            <h3 class="font-bold text-lg text-gray-900 mb-3 flex items-center gap-2">
                                <i data-lucide="brain" class="w-5 h-5 text-primary"></i>
                                Severity of Depression
                            </h3>
                            <p class="text-gray-600">Patients with moderate depression may respond faster than those with severe, long-standing <a href="tms-therapy-for-treatment-resistant-depression.php" class="text-primary hover:underline">treatment-resistant depression</a>. Deeper depression generally involves more entrenched neural patterns that take longer to rewire.</p>
                        </div>

                        <div class="bg-white p-6 rounded-xl border border-gray-200 shadow-sm">
                            <h3 class="font-bold text-lg text-gray-900 mb-3 flex items-center gap-2">
                                <i data-lucide="pills" class="w-5 h-5 text-primary"></i>
                                Concurrent Medications
                            </h3>
                            <p class="text-gray-600">Some antidepressants taken alongside TMS may enhance responsiveness. Conversely, certain benzodiazepines or medications that suppress cortical excitability can dampen TMS effectiveness. Discuss your current <a href="medication-management.php" class="text-primary hover:underline">medication management</a> with your provider.</p>
                        </div>

                        <div class="bg-white p-6 rounded-xl border border-gray-200 shadow-sm">
                            <h3 class="font-bold text-lg text-gray-900 mb-3 flex items-center gap-2">
                                <i data-lucide="calendar-check" class="w-5 h-5 text-primary"></i>
                                Treatment Consistency
                            </h3>
                            <p class="text-gray-600">Missing sessions breaks the cumulative momentum of treatment. Attending every scheduled session is one of the most important things you can do to ensure TMS works as quickly and effectively as possible.</p>
                        </div>

                        <div class="bg-white p-6 rounded-xl border border-gray-200 shadow-sm">
                            <h3 class="font-bold text-lg text-gray-900 mb-3 flex items-center gap-2">
                                <i data-lucide="moon" class="w-5 h-5 text-primary"></i>
                                Sleep & Lifestyle
                            </h3>
                            <p class="text-gray-600">Poor sleep, high stress, alcohol consumption, and physical inactivity can impede the brain changes TMS is trying to promote. Patients who maintain healthy routines alongside treatment tend to respond more favorably.</p>
                        </div>

                        <div class="bg-white p-6 rounded-xl border border-gray-200 shadow-sm">
                            <h3 class="font-bold text-lg text-gray-900 mb-3 flex items-center gap-2">
                                <i data-lucide="dna" class="w-5 h-5 text-primary"></i>
                                Genetics & Neurobiology
                            </h3>
                            <p class="text-gray-600">Individual brain anatomy and genetic factors naturally influence how quickly and robustly a patient responds. Tools like <a href="brain-mapping.php" class="text-primary hover:underline">brain mapping (EEG)</a> can help personalize treatment parameters for better outcomes.</p>
                        </div>

