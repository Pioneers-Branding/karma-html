                <!-- What Is TMS -->
                <section id="what-is-tms" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">What Is TMS Therapy and How Does It Work?</h2>
                    <p class="mb-6">
                        <strong>Transcranial Magnetic Stimulation (TMS)</strong> is an FDA-cleared, non-invasive brain stimulation therapy used primarily for <a href="tms-therapy-for-treatment-resistant-depression.php" class="text-primary hover:underline">treatment-resistant depression</a>. During a session, a trained technician places an electromagnetic coil against your scalp. The coil emits focused magnetic pulses that penetrate the skull and stimulate targeted neurons in the prefrontal cortex — the brain region heavily involved in mood regulation.
                    </p>
                    <p class="mb-6">
                        Over repeated sessions, this stimulation encourages <strong>neuroplasticity</strong> — the brain's ability to reorganize itself by forming new neural connections. This is why TMS doesn't produce instant results: real, lasting changes in brain chemistry and circuit function require cumulative, consistent stimulation over several weeks.
                    </p>
                    <div class="bg-blue-50 border-l-4 border-blue-200 p-6 rounded-r-xl mb-6">
                        <h3 class="font-bold text-blue-900 mb-2">Quick Facts About TMS Treatment</h3>
                        <ul class="list-disc pl-5 space-y-1 text-blue-900">
                            <li>Standard course: <strong>20–36 sessions</strong> over 4–6 weeks</li>
                            <li>Session duration: <strong>20–40 minutes</strong></li>
                            <li>Frequency: <strong>5 days per week</strong> (Monday–Friday)</li>
                            <li>No anesthesia or sedation required</li>
                            <li>Patients can drive themselves and resume normal activities immediately</li>
                        </ul>
                    </div>
                    <p class="mb-6">
                        According to research published in the <a href="https://www.psychiatrist.com/" target="_blank" rel="noopener noreferrer" class="text-primary hover:underline">Journal of Clinical Psychiatry</a>, approximately <strong>50–60%</strong> of patients with treatment-resistant depression respond to TMS, and around <strong>30–35%</strong> achieve full remission. These are compelling numbers for individuals who have tried multiple antidepressants without adequate relief.
                    </p>
                </section>

