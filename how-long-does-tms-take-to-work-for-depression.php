                        </li>
                        <li>
                            <strong>Prioritize sleep.</strong> The brain's natural repair and consolidation processes happen during sleep. Poor sleep directly impairs the neuroplasticity that TMS is trying to stimulate. Aim for 7–9 hours per night.
                        </li>
                        <li>
                            <strong>Exercise regularly.</strong> Physical activity has been shown to boost BDNF (brain-derived neurotrophic factor), a protein that supports neuroplasticity — the same mechanism TMS leverages. Even 30 minutes of moderate walking 5 days per week can make a difference. Review the <a href="four-as-stress-management.php" class="text-primary hover:underline">4 A's of Stress Management</a> for complementary wellness strategies.
