                        <li>
                            <strong>Limit alcohol.</strong> Alcohol is a CNS depressant that can counteract the excitatory effects of TMS. Minimizing or eliminating alcohol during your treatment course is strongly advised.
                        </li>
                        <li>
                            <strong>Continue or start therapy.</strong> Cognitive-behavioral therapy (CBT) and TMS are a potent combination. If you are not currently working with a therapist, ask your Karma TMS provider for a referral.
                        </li>
                        <li>
                            <strong>Manage stress actively.</strong> Chronic stress floods the brain with cortisol, which can impair neuroplasticity. Meditation, breathing exercises, and mindfulness practices can all support TMS outcomes.
                        </li>
