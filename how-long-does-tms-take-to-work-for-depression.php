                        <div class="timeline-content">
                            <h3 class="text-xl font-bold text-gray-900 mb-2">Week 1: Adjustment & Baseline</h3>
                            <p class="text-gray-700 mb-3">During the first week, the primary goal is calibration. Your provider determines the correct magnetic intensity (called the "motor threshold") personalized to your brain. Most patients feel a tapping or clicking sensation on the scalp — it's strange but not painful for most.</p>
                            <p class="text-gray-700"><strong>What to expect:</strong> Little to no change in depression symptoms. Mild scalp discomfort or temporary headache after sessions is normal and usually decreases as you adjust.</p>
                        </div>
                    </div>

                    <div class="timeline-item">
                        <div class="timeline-dot">2</div>
                        <div class="timeline-content">
                            <h3 class="text-xl font-bold text-gray-900 mb-2">Week 2: Early Brain Changes Begin</h3>
                            <p class="text-gray-700 mb-3">By week two, the cumulative effect of sessions begins building. Some particularly responsive patients report subtle improvements: slightly better sleep, a small lift in energy, or noticing they feel a bit "less flat." However, the majority of patients don't notice significant mood changes yet.</p>
                            <p class="text-gray-700"><strong>What to expect:</strong> Possible early, subtle mood shifts. Don't be discouraged if nothing feels different — neuroplastic changes are happening at a cellular level that aren't always consciously detectable yet.</p>
                        </div>
                    </div>

                    <div class="timeline-item">
                        <div class="timeline-dot">3</div>
                        <div class="timeline-content">
                            <h3 class="text-xl font-bold text-gray-900 mb-2">Week 3: The Tipping Point for Many Patients</h3>
                            <p class="text-gray-700 mb-3">Week three is where the majority of patients first notice meaningful improvement. Common reports include improved motivation, returning interest in activities once enjoyed, reduced feelings of hopelessness, and better emotional reactivity. Partners and family members often notice changes before the patient does.</p>
                            <p class="text-gray-700"><strong>What to expect:</strong> Noticeably better mood on some days. You may have good days and not-so-good days — this fluctuation is normal and part of the process.</p>
                        </div>
                    </div>

                    <div class="timeline-item">
                        <div class="timeline-dot">4</div>
                        <div class="timeline-content">
                            <h3 class="text-xl font-bold text-gray-900 mb-2">Week 4: Consolidation of Progress</h3>
                            <p class="text-gray-700 mb-3">By week four, many patients experience a clearer, more consistent improvement in depressive symptoms. The brain circuits targeted by TMS have received substantial cumulative stimulation, reinforcing healthier patterns of neural communication.</p>
                            <p class="text-gray-700"><strong>What to expect:</strong> More consistent mood improvement. Many patients report feeling closer to their "old self." Energy, sleep quality, and concentration often improve alongside mood.</p>
                        </div>
                    </div>

                    <div class="timeline-item">
                        <div class="timeline-dot">5</div>
                        <div class="timeline-content">
                            <h3 class="text-xl font-bold text-gray-900 mb-2">Weeks 5–6: Deep Response & Completion</h3>
                            <p class="text-gray-700 mb-3">The final weeks of the standard treatment course solidify the gains made. Some patients who had partial responses continue to improve. Your psychiatrist will evaluate your response and determine if additional sessions or maintenance TMS is recommended.</p>
                            <p class="text-gray-700"><strong>What to expect:</strong> Sustained improvement, sometimes with further gains after treatment ends. Post-treatment follow-up appointments are important to monitor your ongoing progress.</p>
