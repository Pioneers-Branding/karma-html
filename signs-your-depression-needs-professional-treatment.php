                                </tr>
                                <tr>
                                    <td style="padding:0.85rem 1rem; border-bottom:1px solid #e5e7eb;">Typically lasts days to a week</td>
                                    <td style="padding:0.85rem 1rem; border-bottom:1px solid #e5e7eb;">Persists for 2+ weeks</td>
