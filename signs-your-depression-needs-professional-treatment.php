                                    <th style="padding:1rem; text-align:left; border-bottom:2px solid #e5e7eb; color:#572670; font-weight:700;">Clinical Depression</th>
                                </tr>
                            </thead>
                            <tbody>
                                <tr>
                                    <td style="padding:0.85rem 1rem; border-bottom:1px solid #e5e7eb;">Usually tied to a specific event</td>
