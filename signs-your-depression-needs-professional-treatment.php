                <!-- 10 Warning Signs -->
                <section id="warning-signs" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">10 Signs Your Depression Needs Professional Treatment</h2>
                    <p class="mb-8">
                        The following signs suggest that your depression has moved beyond what self-help strategies, exercise, or "waiting it out" can effectively address:
                    </p>

                    <div class="warning-sign">
                        <div class="warning-number">1</div>
                        <div>
                            <h3 class="font-bold text-gray-900 mb-1">Your Symptoms Have Lasted More Than Two Weeks</h3>
                            <p class="text-gray-600">If persistent low mood, emptiness, or loss of interest has been present nearly every day for two or more weeks, this meets the minimum threshold for clinical depression. Duration is one of the most reliable indicators that professional intervention is needed.</p>
                        </div>
                    </div>

                    <div class="warning-sign">
                        <div class="warning-number">2</div>
                        <div>
                            <h3 class="font-bold text-gray-900 mb-1">You Can No Longer Function at Work or School</h3>
                            <p class="text-gray-600">When depression begins to noticeably impair your ability to concentrate, meet deadlines, remember information, or engage with colleagues, it has crossed into the territory of a disabling condition. Untreated, this can spiral into job loss, academic failure, and financial hardship.</p>
                        </div>
                    </div>

                    <div class="warning-sign">
                        <div class="warning-number">3</div>
                        <div>
                            <h3 class="font-bold text-gray-900 mb-1">Your Relationships Are Significantly Affected</h3>
                            <p class="text-gray-600">Social withdrawal, chronic irritability, emotional numbness, and inability to communicate or connect are common depression symptoms that strain family bonds, friendships, and romantic partnerships. If people close to you have expressed concern or your relationships are deteriorating, it's time to seek help.</p>
                        </div>
                    </div>

                    <div class="warning-sign">
                        <div class="warning-number">4</div>
                        <div>
                            <h3 class="font-bold text-gray-900 mb-1">You Are Experiencing Sleep Extremes</h3>
                            <p class="text-gray-600">Both insomnia (inability to sleep or frequent waking) and hypersomnia (sleeping 10+ hours yet still feeling exhausted) are classic clinical depression symptoms. Sleep disruption exacerbates depression, creating a vicious cycle that becomes harder to break without treatment.</p>
                        </div>
                    </div>

                    <div class="warning-sign">
                        <div class="warning-number">5</div>
                        <div>
                            <h3 class="font-bold text-gray-900 mb-1">You Have Lost Interest in Everything You Once Enjoyed</h3>
                            <p class="text-gray-600"><strong>Anhedonia</strong> — the inability to feel pleasure from activities that once brought joy — is one of the hallmark symptoms of MDD. If hobbies, socializing, food, sex, or even your favorite TV shows no longer bring any positive feeling, that's a strong clinical warning sign.</p>
                        </div>
                    </div>

                    <div class="warning-sign">
                        <div class="warning-number">6</div>
                        <div>
                            <h3 class="font-bold text-gray-900 mb-1">You Are Using Substances to Cope</h3>
                            <p class="text-gray-600">Turning to alcohol, cannabis, or other substances to numb emotional pain or "feel normal" is a major warning sign. Substance use and depression frequently co-occur, and each worsens the other. Professional treatment addresses both simultaneously. Learn about <a href="addiction.php" class="text-primary hover:underline">dual-diagnosis care</a>.</p>
                        </div>
                    </div>

                    <div class="warning-sign">
                        <div class="warning-number">7</div>
                        <div>
                            <h3 class="font-bold text-gray-900 mb-1">Your Physical Health Is Deteriorating</h3>
                            <p class="text-gray-600">Depression is not purely psychological — it has real physical manifestations. Unexplained fatigue, chronic pain, headaches, digestive issues, and significant unintentional weight changes are all recognized physical symptoms of clinical depression. If your doctor has ruled out other medical causes, depression may be the culprit.</p>
                        </div>
                    </div>

                    <div class="warning-sign">
                        <div class="warning-number">8</div>
                        <div>
                            <h3 class="font-bold text-gray-900 mb-1">Antidepressants or Therapy Alone Aren't Working</h3>
                            <p class="text-gray-600">If you've tried one or more antidepressants without achieving adequate symptom relief — or if side effects have made them intolerable — you may be dealing with <a href="tms-therapy-for-treatment-resistant-depression.php" class="text-primary hover:underline">treatment-resistant depression (TRD)</a>. Up to 30% of people with MDD don't respond adequately to standard treatments. Advanced options like TMS exist specifically for this population.</p>
                        </div>
                    </div>

                    <div class="warning-sign">
                        <div class="warning-number">9</div>
                        <div>
                            <h3 class="font-bold text-gray-900 mb-1">You Feel Hopeless or Worthless</h3>
                            <p class="text-gray-600">Persistent feelings of worthlessness, excessive or inappropriate guilt, and an overwhelming sense that things will never improve are core symptoms of MDD and are also risk factors for suicidal ideation. These feelings are symptoms of an illness distorting your perception — they are not the truth.</p>
                        </div>
                    </div>

                    <div class="warning-sign" style="border-left: 4px solid #dc2626;">
                        <div class="warning-number" style="background:#dc2626;">10</div>
                        <div>
                            <h3 class="font-bold text-gray-900 mb-1">You Are Having Thoughts of Death or Suicide</h3>
                            <p class="text-gray-600">Any passive or active thoughts about death, self-harm, or suicide require immediate professional intervention. This is a psychiatric emergency. Please contact a mental health crisis line (call/text <strong>988</strong>), visit an emergency room, or call a trusted person right now. These thoughts are a symptom of a treatable illness — they can and do get better with proper care.</p>
                        </div>
                    </div>
                </section>

                <!-- High-Risk Populations -->
                <section id="high-risk" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">Populations at Elevated Risk of Undertreated Depression</h2>
                    <p class="mb-6">
                        Certain groups face higher rates of depression and, simultaneously, greater barriers to seeking care. If you belong to any of these populations, proactive awareness is especially important:
                    </p>
                    <ul class="list-disc pl-6 space-y-3 text-gray-700 mb-6">
                        <li><strong>Veterans and active military:</strong> Combat exposure, moral injury, and traumatic brain injury create unique depression pathways. <a href="how-to-help-veterans-ptsd.php" class="text-primary hover:underline">Learn about TMS for veterans.</a></li>
                        <li><strong>Women with perinatal or postpartum depression:</strong> Hormonal shifts during and after pregnancy can trigger severe, persistent depression that is frequently underdiagnosed.</li>
                        <li><strong>Older adults:</strong> Depression in seniors is often misattributed to "normal aging" and goes untreated, despite being associated with faster cognitive decline.</li>
                        <li><strong>Individuals with chronic illness:</strong> Living with conditions like diabetes, heart disease, or chronic pain substantially elevates depression risk and must be treated concurrently.</li>
                        <li><strong>LGBTQ+ individuals:</strong> Minority stress, stigma, and discrimination contribute to significantly higher depression prevalence in LGBTQ+ communities.</li>
                        <li><strong>Adolescents:</strong> Teenage depression often manifests as irritability or behavioral changes rather than visible sadness, and is frequently overlooked by parents and educators.</li>
                    </ul>
                </section>

                <!-- Barriers -->
                <section id="barriers" class="mb-12 scroll-target">
