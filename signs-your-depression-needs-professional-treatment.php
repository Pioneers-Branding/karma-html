                <section id="sadness-vs-depression" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">Sadness vs. Clinical Depression: Understanding the Difference</h2>
                    <p class="mb-6">
                        Before listing the warning signs, it helps to understand what separates normal sadness from clinical depression. Both can feel similarly painful in the moment, but they differ in key ways:
                    </p>

                    <div class="overflow-x-auto rounded-xl border border-gray-200 mb-8">
                        <table style="width:100%; border-collapse:collapse; font-size:0.95rem;">
                            <thead>
                                <tr style="background:#f9fafb;">
