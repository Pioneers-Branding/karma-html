                        According to diagnostic criteria from the <a href="https://www.psychiatry.org/psychiatrists/practice/dsm" target="_blank" rel="noopener noreferrer" class="text-primary hover:underline">DSM-5 (Diagnostic and Statistical Manual of Mental Disorders)</a>, a diagnosis of Major Depressive Disorder (MDD) requires at least <strong>5 of 9 specific symptoms</strong> present nearly every day for <strong>at least 2 consecutive weeks</strong>, with at least one symptom being depressed mood or loss of interest/pleasure.
