        // Simple FAQ toggle
        window.toggleBlogFAQ = function(button) {
            const currentItem = button.parentElement;
            const isAlreadyOpen = currentItem.getAttribute('data-state') === 'open';
            document.querySelectorAll('.accordion-item').forEach(item => {
                item.setAttribute('data-state', 'closed');
                const content = item.querySelector('.accordion-content');
                if (content) content.style.maxHeight = '0px'; 
            });
            if (!isAlreadyOpen) {
                currentItem.setAttribute('data-state', 'open');
                const content = currentItem.querySelector('.accordion-content');
                if (content) content.style.maxHeight = content.scrollHeight + 'px';
            }
        };
        
        // Scroll spy
        function scrollToSection(id) {
            const element = document.getElementById(id);
            if (element) {
                window.scrollTo({
                    top: element.offsetTop - 100,
                    behavior: 'smooth'
                });
                
                // Update active state manually
                document.querySelectorAll('.toc-button').forEach(btn => btn.classList.remove('active'));
                const btn = document.querySelector(`button[onclick="scrollToSection('${id}')"]`);
                if(btn) btn.classList.add('active');
            }
        }
    </script>
