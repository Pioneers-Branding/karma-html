                <!-- Section: Understanding Schizophrenia -->
                <section id="understanding" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">Understanding Schizophrenia: Beyond the Myths</h2>
                    <p class="mb-6">
                        Schizophrenia is a chronic brain-based mental health disorder that affects how a person thinks, feels, and interprets reality. It is <strong>not</strong> a split personality disorder (a common misconception). It involves disruptions in perception, thought processing, emotional regulation, and behavior.
                    </p>
                    <p class="mb-6">
                        Most individuals are diagnosed between their late teens and early 30s. Men often show symptoms earlier than women.
                    </p>
                    <div class="bg-gray-50 border-l-4 border-primary p-6 rounded-r-xl mb-8">
                        <p class="mb-0 text-gray-700 font-semibold">
                            But here’s something important: The first signs of schizophrenia usually begin subtly during what clinicians call the <em>prodromal phase</em>. Recognizing this early stage can significantly improve outcomes.
                        </p>
                    </div>
                </section>

                <!-- Section: The Prodromal Phase -->
                <section id="prodromal" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">The Prodromal Phase: Where It Begins</h2>
                    <p class="mb-6">
                        The <strong>prodromal phase</strong> is the early period before full psychotic symptoms develop. It may last weeks, months, or even years.
                    </p>
                    <p class="mb-4">During this time, symptoms can look like:</p>
                    <ul class="list-disc pl-6 space-y-2 text-gray-700 mb-6">
                        <li><a href="depression.php" class="text-primary hover:underline">Depression</a></li>
                        <li><a href="anxiety.php" class="text-primary hover:underline">Anxiety</a></li>
                        <li>Social withdrawal</li>
                        <li>Academic or work decline</li>
                        <li>Personality changes</li>
                    </ul>
                    <p class="mb-6">
                        This is why early schizophrenia symptoms are often mistaken for typical stress, teenage mood swings, or burnout. Let’s explore what these early warning signs look like in detail.
                    </p>
                </section>

                <!-- Section: 10 Early Warning Signs -->
                <section id="early-signs" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-8">10 Early Warning Signs to Watch For</h2>
                    
                    <div class="space-y-8">
                        <div>
                            <h3 class="text-xl font-bold text-gray-900 mb-3">1. Social Withdrawal and Isolation</h3>
                            <p class="mb-2">One of the earliest red flags is pulling away from others. A person who once enjoyed socializing may suddenly:</p>
                            <ul class="list-disc pl-6 space-y-1 text-gray-700">
                                <li>Avoid friends and family</li>
                                <li>Spend excessive time alone</li>
                                <li>Lose interest in previously enjoyed activities</li>
                                <li>Decline invitations repeatedly</li>
                            </ul>
                            <p class="mt-2 text-sm text-gray-600">It may seem like introversion or stress. But when withdrawal becomes persistent and accompanied by other changes, it warrants attention.</p>
                        </div>

                        <div>
                            <h3 class="text-xl font-bold text-gray-900 mb-3">2. Noticeable Decline in School or Work Performance</h3>
                            <p class="mb-2">Cognitive shifts are often early indicators. You might notice:</p>
                            <ul class="list-disc pl-6 space-y-1 text-gray-700">
                                <li>Difficulty concentrating</li>
                                <li>Trouble following conversations</li>
                                <li>Forgetting tasks</li>
                                <li>Reduced motivation</li>
                                <li>Poor academic or job performance</li>
                            </ul>
                            <p class="mt-2 text-sm text-gray-600">Teachers or employers may describe the person as “not trying” or “not focused.” In reality, the brain may be struggling with early cognitive disruption.</p>
                        </div>

                        <div>
                            <h3 class="text-xl font-bold text-gray-900 mb-3">3. Subtle Paranoia or Suspicion</h3>
                            <p class="mb-2">Early paranoia can appear mild at first. Examples include:</p>
                            <ul class="list-disc pl-6 space-y-1 text-gray-700">
                                <li>Feeling watched</li>
                                <li>Believing others are talking about them</li>
                                <li>Overinterpreting harmless comments</li>
                                <li>Increasing distrust without clear reason</li>
                            </ul>
                            <p class="mt-2 text-sm text-gray-600">At this stage, beliefs may not be fully delusional, but they often feel intense and emotionally real.</p>
                        </div>

                        <div>
                            <h3 class="text-xl font-bold text-gray-900 mb-3">4. Changes in Thinking Patterns</h3>
                            <p class="mb-2">Thoughts may become:</p>
                            <ul class="list-disc pl-6 space-y-1 text-gray-700">
                                <li>Tangential (jumping between topics)</li>
                                <li>Illogical</li>
                                <li>Hard to follow</li>
                                <li>Disorganized</li>
                            </ul>
                            <p class="mt-2 text-sm text-gray-600">Family members might say: “Your conversations don’t make sense lately.” The person themselves may feel confused or overwhelmed by their own thoughts.</p>
                        </div>

                         <div>
                            <h3 class="text-xl font-bold text-gray-900 mb-3">5. Emotional Changes or Flat Affect</h3>
                            <p class="mb-2">Early schizophrenia can impact emotional expression. Possible signs include:</p>
                            <ul class="list-disc pl-6 space-y-1 text-gray-700">
                                <li>Reduced facial expression</li>
                                <li>Monotone speech</li>
                                <li>Limited emotional reactions</li>
                                <li>Appearing emotionally distant</li>
                            </ul>
                            <p class="mt-2 text-sm text-gray-600">This is sometimes called flat affect and is part of what clinicians refer to as “negative symptoms.”</p>
                        </div>

                        <div>
                            <h3 class="text-xl font-bold text-gray-900 mb-3">6. Sleep Disturbances</h3>
                            <p class="mb-2">Sleep often changes early. Common patterns include:</p>
                            <ul class="list-disc pl-6 space-y-1 text-gray-700">
                                <li>Insomnia</li>
                                <li>Reversed sleep schedule</li>
                                <li>Nighttime restlessness</li>
                                <li>Sleeping excessively</li>
                            </ul>
                            <p class="mt-2 text-sm text-gray-600">Sleep disruption can intensify other symptoms and accelerate progression.</p>
                        </div>

                         <div>
                            <h3 class="text-xl font-bold text-gray-900 mb-3">7. Heightened Anxiety or Depression</h3>
                            <p class="mb-2">Early schizophrenia symptoms frequently overlap with mood disorders. You may see:</p>
                            <ul class="list-disc pl-6 space-y-1 text-gray-700">
                                <li>Panic attacks</li>
                                <li>Persistent sadness</li>
                                <li>Irritability</li>
                                <li>Feeling detached from reality</li>
                                <li>Loss of pleasure</li>
                            </ul>
                            <p class="mt-2 text-sm text-gray-600">In fact, many individuals are initially treated for depression or anxiety before psychotic symptoms fully emerge.</p>
                        </div>

                         <div>
                            <h3 class="text-xl font-bold text-gray-900 mb-3">8. Unusual Perceptual Experiences</h3>
                            <p class="mb-2">Before full hallucinations appear, subtle perceptual distortions may occur:</p>
                            <ul class="list-disc pl-6 space-y-1 text-gray-700">
                                <li>Hearing faint whispers</li>
                                <li>Seeing shadows in peripheral vision</li>
                                <li>Feeling someone is present when alone</li>
                                <li>Heightened sensitivity to light or sound</li>
                            </ul>
                            <p class="mt-2 text-sm text-gray-600">These experiences may be brushed off initially, but they’re significant when recurring.</p>
                        </div>

                         <div>
                            <h3 class="text-xl font-bold text-gray-900 mb-3">9. Neglect of Personal Hygiene</h3>
                            <p class="mb-2">Another early behavioral change is reduced self-care:</p>
                            <ul class="list-disc pl-6 space-y-1 text-gray-700">
                                <li>Not showering</li>
                                <li>Wearing the same clothes repeatedly</li>
                                <li>Ignoring grooming</li>
                                <li>Lack of concern about appearance</li>
                            </ul>
                            <p class="mt-2 text-sm text-gray-600">This may be mistaken for laziness or depression.</p>
                        </div>

                         <div>
                            <h3 class="text-xl font-bold text-gray-900 mb-3">10. Difficulty Distinguishing Reality from Imagination</h3>
                            <p class="mb-2">As symptoms progress, distinguishing internal thoughts from external reality becomes harder. The person may:</p>
                            <ul class="list-disc pl-6 space-y-1 text-gray-700">
                                <li>Question what’s real</li>
                                <li>Express unusual beliefs</li>
                                <li>Become deeply preoccupied with abstract ideas</li>
                            </ul>
                            <p class="mt-2 text-sm text-gray-600">This marks a transition toward the active phase of schizophrenia.</p>
                        </div>
                    </div>
                </section>

                <!-- Section: When Do Full Symptoms Develop? -->
                <section id="full-symptoms" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">When Do Full Symptoms Develop?</h2>
                    <p class="mb-4">
                        If untreated, early signs can progress into active psychosis, which may include:
                    </p>
                    <ul class="list-disc pl-6 space-y-2 text-gray-700 mb-6">
                        <li><strong>Hallucinations:</strong> Hearing voices is most common.</li>
                        <li><strong>Delusions:</strong> Fixed false beliefs.</li>
                        <li><strong>Severely disorganized speech.</strong></li>
                        <li><strong>Catatonia:</strong> In rare cases.</li>
                    </ul>
                    <p class="mb-6">
                        This stage often leads families to seek urgent psychiatric care. But here’s the hopeful part: <strong>Early intervention dramatically improves long-term outcomes.</strong>
                    </p>
                </section>

                <!-- Section: Risk Factors -->
                <section id="risk-factors" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">Risk Factors for Schizophrenia</h2>
                    <p class="mb-6">While there’s no single cause, several factors increase risk:</p>
                    <div class="grid md:grid-cols-2 gap-6">
                        <div class="bg-gray-50 p-6 rounded-xl">
                            <h3 class="font-bold text-lg mb-2 text-gray-900">Genetics</h3>
                            <p class="text-sm">Having a first-degree relative with schizophrenia raises risk.</p>
                        </div>
                        <div class="bg-gray-50 p-6 rounded-xl">
                            <h3 class="font-bold text-lg mb-2 text-gray-900">Brain Chemistry</h3>
                            <p class="text-sm">Imbalances in dopamine and glutamate are involved.</p>
                        </div>
                         <div class="bg-gray-50 p-6 rounded-xl md:col-span-2">
                            <h3 class="font-bold text-lg mb-2 text-gray-900">Environmental Stressors</h3>
                            <ul class="list-disc pl-6 space-y-1 text-sm">
                                <li>Trauma</li>
                                <li>Substance use (especially cannabis in adolescence)</li>
                                <li>Prenatal complications</li>
                                <li>Severe stress</li>
                            </ul>
                        </div>
                    </div>
