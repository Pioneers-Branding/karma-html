        }

        // Handle Side Effect Checkbox
        function handleSideEffectCheck(effect, checked) {
            if (checked) {
                trackedSideEffects[effect] = true;
            } else {
                delete trackedSideEffects[effect];
            }
            updateSummary();
        }

        // Update Summary
        function updateSummary() {
            const totalTracked = Object.keys(trackedSideEffects).length;
            const totalEffects = 32;
            const percentage = (totalTracked / totalEffects) * 100;

            document.getElementById('tracked-count').textContent = totalTracked;
            document.getElementById('progress-bar').style.width = `${percentage}%`;

            const alertBox = document.getElementById('alert-box');

            if (totalTracked === 0) {
                alertBox.innerHTML = `
                    <div class="bg-purple-100 border border-violet-300 rounded-lg p-4">
                        <p class="text-purple-800 font-semibold text-center">
                            âœ… Excellent! No significant side effects reported.
                        </p>
                    </div>
                `;
            } else if (totalTracked <= totalEffects * 0.25) {
                alertBox.innerHTML = `
                    <div class="bg-yellow-100 border border-yellow-300 rounded-lg p-4">
                        <p class="text-yellow-800 font-semibold text-center">
                            âš ï¸ Mild side effects - Let's discuss optimization strategies.
                        </p>
                    </div>
                `;
            } else if (totalTracked <= totalEffects * 0.5) {
                alertBox.innerHTML = `
                    <div class="bg-violet-100 border border-purple-300 rounded-lg p-4">
                        <p class="text-purple-800 font-semibold text-center">
                            âš ï¸ Moderate side effect burden - Medication review recommended.
                        </p>
                    </div>
                `;
            } else {
                alertBox.innerHTML = `
                    <div class="bg-red-100 border border-red-300 rounded-lg p-4">
                        <p class="text-red-800 font-semibold text-center">
                            ðŸš¨ Significant side effects - Urgent medication review needed.
                        </p>
                    </div>
                `;
            }
        }

