                                    onchange="handleSideEffectCheck('${effect}', this.checked)">
                                <label for="effect-${categoryIndex}-${effectIndex}"
                                    class="text-sm text-gray-700 leading-relaxed cursor-pointer">
                                    ${effect}
                                </label>
                            </div>
