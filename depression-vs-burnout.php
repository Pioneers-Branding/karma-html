                <!-- What Is Burnout -->
                <section id="what-is-burnout" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">What Is Burnout?</h2>
                    <p class="mb-6">
                        Burnout was officially classified as an occupational phenomenon by the <a href="https://www.who.int/news/item/28-05-2019-burn-out-an-occupational-phenomenon-international-classification-of-diseases" target="_blank" rel="noopener noreferrer" class="text-primary hover:underline">World Health Organization (WHO)</a> in 2019, included in the ICD-11 as a syndrome resulting from chronic workplace stress that has not been successfully managed.
                    </p>
                    <p class="mb-6">The WHO defines burnout through three core dimensions:</p>

                    <div class="grid md:grid-cols-3 gap-4 mb-8">
                        <div class="bg-white p-5 rounded-xl border border-gray-200 shadow-sm text-center">
                            <div style="width:3rem;height:3rem;background:#572670;border-radius:50%;display:flex;align-items:center;justify-content:center;margin:0 auto 1rem;">
                                <i data-lucide="battery-low" class="w-6 h-6 text-white"></i>
                            </div>
                            <h3 class="font-bold text-gray-900 mb-2">Energy Depletion</h3>
                            <p class="text-gray-600 text-sm">Feelings of exhaustion and being completely drained of physical and emotional resources</p>
                        </div>
                        <div class="bg-white p-5 rounded-xl border border-gray-200 shadow-sm text-center">
                            <div style="width:3rem;height:3rem;background:#572670;border-radius:50%;display:flex;align-items:center;justify-content:center;margin:0 auto 1rem;">
                                <i data-lucide="user-x" class="w-6 h-6 text-white"></i>
                            </div>
                            <h3 class="font-bold text-gray-900 mb-2">Increased Mental Distance</h3>
                            <p class="text-gray-600 text-sm">Cynicism, negativism, or detachment from one's job and the people at work</p>
                        </div>
                        <div class="bg-white p-5 rounded-xl border border-gray-200 shadow-sm text-center">
                            <div style="width:3rem;height:3rem;background:#572670;border-radius:50%;display:flex;align-items:center;justify-content:center;margin:0 auto 1rem;">
                                <i data-lucide="trending-down" class="w-6 h-6 text-white"></i>
                            </div>
                            <h3 class="font-bold text-gray-900 mb-2">Reduced Efficacy</h3>
                            <p class="text-gray-600 text-sm">Diminished sense of professional accomplishment and confidence in one's abilities</p>
                        </div>
                    </div>

                    <div class="bg-amber-50 border-l-4 border-amber-200 p-6 rounded-r-xl mb-6">
                        <h4 class="font-bold text-amber-900 mb-2">Important Nuance</h4>
                        <p class="text-amber-900">Burnout, by the WHO's definition, is <strong>context-specific</strong>. It refers to phenomena in the occupational context specifically and is not classified as a medical condition or mental disorder. This is a critical distinction when it comes to treatment.</p>
                    </div>

                    <p class="mb-6">
                        Beyond classic workplace burnout, clinicians frequently see burnout triggered by other chronic overload contexts: caregiver burnout, parental burnout, and academic burnout all share the same core triad of exhaustion, cynicism, and reduced efficacy.
                    </p>

                    <h3 class="text-2xl font-bold text-gray-900 mb-4">Common Burnout Symptoms</h3>
                    <ul class="list-disc pl-6 space-y-2 text-gray-700 mb-6">
                        <li>Persistent exhaustion that doesn't improve with rest</li>
                        <li>Cynicism, detachment, or resentment toward work or responsibilities</li>
                        <li>Decreased productivity and difficulty concentrating at work</li>
                        <li>Feeling like everything is a chore; dreading going to work</li>
                        <li>Irritability, short temper, or emotional outbursts</li>
                        <li>Physical symptoms: frequent illness, headaches, muscle tension</li>
                        <li>Neglecting personal needs (skipping meals, poor sleep, social isolation)</li>
                        <li>Questioning the meaning or value of your work</li>
                    </ul>

                    <p class="mb-6">
                        <strong>Key distinguishing feature of burnout:</strong> These symptoms are primarily tied to one domain (usually work or a specific caregiving role). Remove or significantly reduce that stressor, and the person typically begins to recover.
                    </p>
                </section>

