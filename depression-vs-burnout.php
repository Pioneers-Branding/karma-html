                <section id="what-is-depression" class="mb-12 scroll-target">
                    <h2 class="text-3xl font-bold text-primary mb-6">What Is Clinical Depression?</h2>
                    <p class="mb-6">
                        <strong>Major Depressive Disorder (MDD)</strong> is a recognized medical condition in which disruptions in brain chemistry — particularly involving serotonin, norepinephrine, and dopamine systems — produce a pervasive and persistent low mood that affects virtually every domain of a person's life.
                    </p>
                    <p class="mb-6">
                        According to <a href="https://www.psychiatry.org/" target="_blank" rel="noopener noreferrer" class="text-primary hover:underline">the American Psychiatric Association</a>, MDD is diagnosed when a person experiences at least 5 of the following 9 symptoms nearly every day for at least 2 consecutive weeks:
                    </p>

                    <div class="grid md:grid-cols-2 gap-3 mb-8">
                        <div class="flex items-start gap-3 bg-white p-4 rounded-xl border border-gray-200 shadow-sm">
                            <i data-lucide="minus-circle" class="w-5 h-5 text-primary mt-0.5 flex-shrink-0"></i>
                            <span class="text-gray-700">Depressed mood most of the day</span>
                        </div>
                        <div class="flex items-start gap-3 bg-white p-4 rounded-xl border border-gray-200 shadow-sm">
                            <i data-lucide="minus-circle" class="w-5 h-5 text-primary mt-0.5 flex-shrink-0"></i>
                            <span class="text-gray-700">Loss of interest or pleasure in nearly all activities (anhedonia)</span>
                        </div>
                        <div class="flex items-start gap-3 bg-white p-4 rounded-xl border border-gray-200 shadow-sm">
                            <i data-lucide="minus-circle" class="w-5 h-5 text-primary mt-0.5 flex-shrink-0"></i>
                            <span class="text-gray-700">Significant weight or appetite changes</span>
                        </div>
                        <div class="flex items-start gap-3 bg-white p-4 rounded-xl border border-gray-200 shadow-sm">
                            <i data-lucide="minus-circle" class="w-5 h-5 text-primary mt-0.5 flex-shrink-0"></i>
                            <span class="text-gray-700">Insomnia or hypersomnia</span>
                        </div>
                        <div class="flex items-start gap-3 bg-white p-4 rounded-xl border border-gray-200 shadow-sm">
                            <i data-lucide="minus-circle" class="w-5 h-5 text-primary mt-0.5 flex-shrink-0"></i>
                            <span class="text-gray-700">Psychomotor agitation or retardation</span>
                        </div>
                        <div class="flex items-start gap-3 bg-white p-4 rounded-xl border border-gray-200 shadow-sm">
                            <i data-lucide="minus-circle" class="w-5 h-5 text-primary mt-0.5 flex-shrink-0"></i>
                            <span class="text-gray-700">Fatigue or loss of energy</span>
                        </div>
                        <div class="flex items-start gap-3 bg-white p-4 rounded-xl border border-gray-200 shadow-sm">
                            <i data-lucide="minus-circle" class="w-5 h-5 text-primary mt-0.5 flex-shrink-0"></i>
                            <span class="text-gray-700">Feelings of worthlessness or excessive guilt</span>
                        </div>
                        <div class="flex items-start gap-3 bg-white p-4 rounded-xl border border-gray-200 shadow-sm">
                            <i data-lucide="minus-circle" class="w-5 h-5 text-primary mt-0.5 flex-shrink-0"></i>
                            <span class="text-gray-700">Difficulty thinking, concentrating, or making decisions</span>
                        </div>
                        <div class="flex items-start gap-3 bg-white p-4 rounded-xl border border-gray-200 shadow-sm" style="grid-column: span 2;">
                            <i data-lucide="minus-circle" class="w-5 h-5 text-primary mt-0.5 flex-shrink-0"></i>
                            <span class="text-gray-700">Recurrent thoughts of death or suicidal ideation</span>
                        </div>
                    </div>

                    <p class="mb-6">
                        <strong>Key distinguishing feature of depression:</strong> Symptoms are pervasive — they bleed into all areas of life, not just work. Even activities once loved (family time, hobbies, vacation) bring little to no joy. Depression persists regardless of circumstances.
                    </p>
                </section>

